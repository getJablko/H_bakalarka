    private void displayDataInTable() {
        try {
            transaction.begin();

            // ziskanie dat - JPQL
            TypedQuery<Object[]> query = entityManager.createQuery(
                    "SELECT u.idPoruchy, u.osCisloOpravy, u.prebratiePoruchy, u.dobaOpravy, u.popisUdrzby, u.pricinaPoruchy, str.typStroja, p.idStroja " +
                            "FROM BUdrzbaPoruchy u " +
                            "JOIN BPorucha p on p.idPoruchy = u.idPoruchy " +
                            "JOIN BStroj str on str.idStroja = p.idStroja", Object[].class);

            List<Object[]> results = query.getResultList();

            // nacitanie udajov
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            for (Object[] result : results) {
                Object[] row = {
                        result[0],  // idPoruchy
                        result[1],  // osCisloOpravy
                        result[2],  // prebratie
                        result[3],  // doobaOpravy
                        result[4],  // popisUdrzby
                        result[5],  // pricinyPoruchy
                        result[6],  // typ Stroja
                        result[7],  // id stroja
                };
                model.addRow(row);
            }
            transaction.commit();

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Nastala chyba pri načítavaní údajov: " + e.getMessage() + " Skúste to znovu!");
            if (transaction.isActive()) {
                transaction.rollback();
            }
        }
    }

