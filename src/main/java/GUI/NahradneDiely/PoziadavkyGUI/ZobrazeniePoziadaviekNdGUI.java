    private void displayDataInTable() {
        try {
            transaction.begin();

            // ziskanie dat - JPQL
            TypedQuery<Object[]> query = entityManager.createQuery(
                    "SELECT DISTINCT up.idPoruchy, up.osCisloOpravy, up.cisloNd, up.pozadavkaNd, nd.nazovND, nd.dostupneMnozstvo,up.pozadovaneMnozstvo " +
                            "FROM BUdrzbaPoruchyNahradnyDiel up " +
                            "JOIN BNahradnyDiel nd on up.cisloNd = nd.cisloNd " +
                            "JOIN BUdrzbaPoruchy u ON u.idPoruchy = up.idPoruchy AND u.osCisloOpravy = up.osCisloOpravy " +
                            "WHERE up.pripravene = 0 " , Object[].class);

            List<Object[]> results = query.getResultList();

            // nacitanie udajov
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            for (Object[] result : results) {
                Object[] row = {
                        result[0],  // idPoruchy
                        result[1],  // osCisloOpravy
                        result[2],  // cisloND
                        result[3],  // poziadavka
                        result[4],  // nazovND
                        result[5],  // dostupneMnozstvo
                        result[6],  // pozadovaneMnozstvo
                };
                model.addRow(row);
            }
            transaction.commit();

        } catch (Exception e) {
            JOptionPane.getRootFrame().setAlwaysOnTop(true);
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Nastala chyba pri načítavaní údajov: " + e.getMessage() + " Skúste to znovu!");
            if (transaction.isActive()) {
                transaction.rollback();
            }
        }
        JOptionPane.getRootFrame().setAlwaysOnTop(false);
    }

